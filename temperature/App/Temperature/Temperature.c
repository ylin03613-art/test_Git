/*********************************************************************************************************
* 函数名称: ProbeConnection
* 函数功能: 探头连接电路 
* 输入参数: 
* 输出参数: 
* 返 回 值: 1高电平-未接入 0低电平-已接入 LED反馈
* 创建日期: 
* 注    意: LED反馈
*********************************************************************************************************/
void ProbeConnection(void)
{
  static u8 lastLEDState = Bit_RESET; //上一次LED灯的状态
  printf("lead off:%d\n",leadFlag);
  DelayNms(2);
  if(GPIO_ReadInputDataBit(GPIOC, SENS_OFF))    //未接入探头时是高电平
  {
    if(lastLEDState != Bit_RESET)  //导联状态发生变化时修改gpio
    {
      GPIO_WriteBit(GPIOC, GPIO_Pin_5, Bit_RESET);          //将LED2的状态设置为熄灭
      printf("探头未接入\n");
      lastLEDState = Bit_RESET;
    }
    leadFlag = 1; //将导联脱落标志位置1 已脱离
  }
  
  else  //接入探头时是低电平，点亮LED2
  {
    if(lastLEDState != Bit_SET)
    {
      GPIO_WriteBit(GPIOC, GPIO_Pin_5, Bit_SET);          //将LED2的状态设置为点亮
      lastLEDState = Bit_SET;
    }
    leadFlag = 0;  //将导联脱落标志位置0 未脱离
  }
  printf("lead off:%d\n",leadFlag);
}
