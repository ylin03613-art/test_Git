/*********************************************************************************************************
* 函数名称：BinarySearch
* 函数功能：二分法查找函数
* 输入参数：arr[]-已排序的数组 n-数组长度 target-目标值
* 输出参数：void
* 返 回 值：目标值所在的索引
* 创建日期：
* 注    意：若目标值存在返回目标值所在的索引，若目标值不存在返回与目标值最接近的索引
*********************************************************************************************************/
static u16 BinarySearch(float arr[], u16 n, float target)
{
  u16 left = 0; //左指针变量
  u16 right = n - 1;  //右指针变量
  u16 index = 0; //记录最接近目标中的索引
  float currentDiff;  //当前值和目标值的差值 
  float diff = fabs(arr[0] - target);  //记录最接近目标值的差值
  u16 mid = 0;  //数组中间索引值
  
  while(left <= right)  //当左指针变量小于或等于右指针变量时进入循环
  {
    mid = left + (right - left) / 2;  //计算中间索引
    if(arr[mid] == target)  //如果找到目标值
    {
      return mid;           //直接返回位置
    }
    
    currentDiff = fabs(arr[mid] - target);  //计算当前值和目标值的差值
    if(currentDiff < diff)   //当前差值比最近差值小时
    {
      diff = currentDiff;   //最近差值等于当前差值
      index = mid;          //记录索引
      //printf("BinarySearch-index=%d\n",index);
    }
    
    if(arr[mid] > target)  //当目标值比中间值大时，数组是从大到小排序
    {
      left = mid + 1;      //往右半部分找
    }
    else                   //当目标值比中间值小时
    {
      right = mid - 1;     //往左半部分找
    }
  }
  //printf("BinarySearch-return-index=%d\n",index);
  //没有找到目标值时，返回最接近目标值的索引
  return index;
  
}
  
